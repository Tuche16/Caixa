    private void btConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConectarActionPerformed
        String password=String.valueOf(campoSenha.getPassword());
        rst=this.consulta.login(this.campoUsuario.getText(), password);
        try{
            rst.last();
            int numLinhas = rst.getRow();
            rst.beforeFirst();
            if(numLinhas>=1){
                rst.next();
                InterfacePrincipal abrir=new InterfacePrincipal(this.campoUsuario.getText(), rst.getString("nomeCompleto"), rst.getBoolean("permissao"));
                abrir.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"Usu치rio e/ou senha incorreto(s)!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btConectarActionPerformed

