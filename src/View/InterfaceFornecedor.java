    private void btBuscarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarFornecedorActionPerformed
        DefaultTableModel dm = (DefaultTableModel) this.tbFornecedor.getModel();
        if(this.comboFormaPesquisa.getSelectedIndex()==0&&this.tbFornecedor.getRowCount()<0){
            this.listarFornecedores(2);
        }
        else if(this.comboFormaPesquisa.getSelectedIndex()==1&&!this.campoNomeCod.getText().isEmpty()){
            int rowCount = dm.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                dm.removeRow(i);
            }
            rst=this.fornecedorController.buscarFornecedor(this.campoNomeCod.getText(), 0);
            DefaultTableModel model = (DefaultTableModel) this.tbFornecedor.getModel();
            try{
                model.addRow(new Object[]{rst.getInt("idFornecedor"),rst.getString("razaoSocial"),rst.getString("cnpj"),rst.getString("endereco"),rst.getString("tel"),rst.getString("dataCadastro"),rst.getString("outros")});
                while(rst.next()){
                    model.addRow(new Object[]{rst.getInt("idFornecedor"),rst.getString("razaoSocial"),rst.getString("cnpj"),rst.getString("endereco"),rst.getString("tel"),rst.getString("dataCadastro"),rst.getString("outros")});
                }
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null,"NÃ£o foi encontrado nenhum fornecedor com essa razÃ£o social!","ATENÃÃO",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else if(this.comboFormaPesquisa.getSelectedIndex()==2&&!this.campoNomeCod.getText().isEmpty()){
            int rowCount = dm.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                dm.removeRow(i);
            }
            rst=this.fornecedorController.buscarFornecedor("null", Integer.parseInt(this.campoNomeCod.getText()));
            DefaultTableModel model = (DefaultTableModel) this.tbFornecedor.getModel();
            try{
                model.addRow(new Object[]{rst.getInt("idFornecedor"),rst.getString("razaoSocial"),rst.getString("cnpj"),rst.getString("endereco"),rst.getString("tel"),rst.getString("dataCadastro"),rst.getString("outros")});
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null,"NÃ£o foi encontrado nenhum fornecedor com esse cÃ³digo!","ATENÃÃO",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment( JLabel.CENTER );
        for(int x=0;x<this.tbFornecedor.getColumnCount();x++){
            this.tbFornecedor.getColumnModel().getColumn(x).setCellRenderer( centerRenderer );
        }
    }//GEN-LAST:event_btBuscarFornecedorActionPerformed

