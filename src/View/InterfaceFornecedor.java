    private void campoRazaoSocialAddKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoRazaoSocialAddKeyReleased
        if(this.campoRazaoSocialAdd.getText().isEmpty()){
            this.btAdicionar.setEnabled(false);
            this.campoCnpjAdd.setEnabled(false);
            this.campoTelAdd.setEnabled(false);
        }
        else if(!this.campoCnpjAdd.getText().contains(" ")&&!this.campoTelAdd.getText().contains(" ")){
            this.campoCnpjAdd.setEnabled(true);
            this.campoTelAdd.setEnabled(true);
            this.btAdicionar.setEnabled(true);
        }
        else if(!this.campoCnpjAdd.getText().contains(" ")){
            this.campoCnpjAdd.setEnabled(true);
        }
        else{
            this.btLimparAdd.setEnabled(true);
            this.campoCnpjAdd.setEnabled(true);
        }
    }//GEN-LAST:event_campoRazaoSocialAddKeyReleased

